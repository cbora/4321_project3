package Project;

/**
 * Represents [low,high] range of values
 * @author Richard Henwood (rbh228)
 * @author Chris Bora (cdb239)
 * @author Han Wen Chen (hc844)
 *
 */
public class Pair {
	////////////////////////////
	// Fields
	////////////////////////////
	
	public int low; // low values
	public int high; // high value
	
	/////////////////////////////
	// Constructor
	////////////////////////////
	/**
	 * Constructor
	 * @param low
	 * @param high
	 */
	public Pair(int low, int high) {
		this.low = low;
		this.high = high;
	}
}
